package game;

/**
 * The enum represents the possible game states <i>(playing, won, lost)</i>.
 *
 * @author Martin Růžek
 * @version 1.0
 */
public enum GameState {
    /**
     * Game is running.
     */
    PLAYING,
    /**
     * Player lost the game.
     */
    LOST,
    /**
     * Player won the game.
     */
    WON
}
